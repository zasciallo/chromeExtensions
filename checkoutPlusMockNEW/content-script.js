function replaceTextInNode(root, newText) {
	const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, null);
	let node;
	while ((node = walker.nextNode())) {
		if (node.nodeValue?.trim()) {
			node.nodeValue = newText;
			break;
		}
	}
}

